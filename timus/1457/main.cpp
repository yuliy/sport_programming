#include <stdio.h>
#include <iostream>
#include <vector>
#include <string>
#include <map>
#include <set>
#include <list>
#include <algorithm>
using namespace std;

int main() {
    int N;
    scanf("%d", &N);

    double res = 0.0;
    for (int i = 0; i < N; ++i) {
        float tmp;
        scanf("%f", &tmp);
        res += tmp;
    }

    res /= N;
    printf("%.6f\n", res);

    return 0;
}
